import re
re_integer = re.compile("^[-+]{0,1}\d*$")

def validator(pattern, promt):
    text = input(promt)
    while not bool(pattern.match(text)):
        text = input(promt)
    return text


def int_number_validator(prompt):

    number = int(validator(re_integer, prompt))

    return number


a = int_number_validator('Enter a: ')
b = int_number_validator('Enter b: ')
c = int_number_validator('Enter c: ')
d = a + b
e = c - a
f = a + b + c
a = d
b = e
c = f
print(a)
print(b)
print(c)
